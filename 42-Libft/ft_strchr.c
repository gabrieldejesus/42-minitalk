char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*temp;

	i = 0;
	temp = (char *) s;
	while (temp[i] != (char)c)
	{
		if (temp[i] == '\0')
			return (0);
		i++;
	}
	return (&temp[i]);
}
